-- create table Parks with summary information about US National Parks
CREATE TABLE Parks
(
	park_id		INTEGER			NOT NULL,
	park_name	VARCHAR(500)	NOT NULL,
	us_state	VARCHAR(500)	NOT NULL,
	acres		FLOAT			NULL,
	visitors	INTEGER			NULL,
	PRIMARY KEY(park_id)
);

-- create table Parks_Founded with EST dates for each park
CREATE TABLE Parks_Founded
(
	park_id		INTEGER			NOT NULL,
	founded		DATE			NOT NULL,
	longitude	FLOAT			NULL,
	latitude	FLOAT			NULL
);

-- create table Park_Ratings with visitor ratings for each park
CREATE TABLE Parks_Rating
(
	park_id			INTEGER			NOT NULL,
	rating			FLOAT			NOT NULL,
	tent_campers	FLOAT			NULL
);

